module SolidRuby::Primitives
  class RawScad < Primitive
    alias_attr :text

    def initialize(attributes)
      if attributes.is_a? String
        attributes = {text: attributes}
      end

      super(attributes)
    end

    def to_rubyscad
      RubyScadBridge.new.raw_scad(@attributes[:text])
    end
  end

  def raw_scad(args)
    RawScad.new(args)
  end
end
